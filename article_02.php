					<h2>New exhibits opening at Casa explore barbed wire, memory and house music</h2>
					<p><img class="icon" src="icons/ic_access_time_black_24dp_1x.png">Saturday, 13 Jan 2017 12:53</p>
					<p><img class="icon" src="icons/ic_person_black_24dp_1x.png">Richard Amery </p>
					<p>Lethbridge based artist Adrian Cooke explores the design and history of barbed wire in his new exhibit “Chasing the Devil’s Rope,” one of five new exhibits opening at Casa tonight, Jan. 13.
					<br><br>
					“Chasing the Devil’s Rope” features sculpture and computer generated watercolour prints inspired by the shape of barbed wire, reflecting the historical role of the  product and it’s design.
					<br><br>
					“Which was first used as a means of dividing property and controlling and isolating people and animals. Further, it was used as a tool of repression and manipulation in times human conflict. A man-made creation, barbed wire encroaches and divides, transforming our interaction with the landscape,” according to Cooke’s artist statement.
					<br><br>
					“ Some of the pieces go back seven years,” Cooke, said, noting some  of the original designs were inspired by the work he did  on the decor for Backstreet West.
					<br><br>
					“Barbed wire has political connotations for First Nations and dividing nature But part of what I was interested in was more the design of it,” he said. “ Even the shadows of the pieces are important,” he noted, adding some of the horn shaped pieces have a violent connotation. “ they are very physical.”
					<br><br>
					He created the sculptures  with OSB board and a digital printer. Two longer watercolour works were hand drawn, scanned and printed  by computer have a wallpaper effect.
					<br><br>
					The larger sculptures are layered OSB board designed like pieces of barbed wire. Cooke worked at the U of L for 28 years in various capacities including  with the U of L art gallery.
					<br><br>
					There are several other exhibitions  opening  at Casa this weekend, including one by Bragg Creek born, Brooklyn educated  Ryan Smitham and Yasunari Izaki called “Accumulation of memory,” in the other half of  the main gallery, Brooklyn artist Yasunari Izaki was delayed by a flight from Brooklyn so will be arriving at the last minute to set up the exhibit in time for the opening reception tonight 7-9 p.m. at Casa. It  is an exploration of objects which reflect memory and time such as tree rings reflecting time through tree rings.
